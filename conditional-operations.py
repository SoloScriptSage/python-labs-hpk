#operator_and
age = 22
weight = 58
result = age > 21 and weight == 58
print(result) #true

#operator_or
isMarried = False
result = age > 21 or isMarried
print(result) #true because age > 21

#operator_not
print(not age > 21) #false
print(not isMarried) #true
print(not 4) #false
print(not 0) #true


